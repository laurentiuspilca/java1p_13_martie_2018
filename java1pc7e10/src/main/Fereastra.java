    private void adaugaPersoana() {
        String url = "jdbc:mysql://localhost/java1pcurs7";
        String user = "root";
        String pass = "";
        
        try (Connection con = 
                DriverManager.getConnection(url, user, pass)) {
            String nume = jTextField1.getText();
            String prenume = jTextField2.getText();
            
            String sql = "INSERT INTO persoane VALUES (NULL, ?, ?)";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setString(1, nume);
            stmt.setString(2, prenume);
            
            stmt.executeUpdate();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

